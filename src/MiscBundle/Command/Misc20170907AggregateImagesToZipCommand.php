  use CommandBaseTrait;

  public $results = [];

  protected function configure()
  {
    $this
      ->setName('misc:20170907-aggregate-images-to-zip')
      ->setDescription('臨時 ppm 商品画像zip圧縮処理');
  }

  protected function execute(InputInterface $input, OutputInterface $output)
  {
    $this->setInput($input);
    $this->setOutput($output);

    $logger = $this->getLogger();

    $this->results = [];

    /** @var FileUtil $fileUtil */
    $fileUtil = $this->getContainer()->get('misc.util.file');

    try {
      $fileList = [];
      foreach($this->targetProducts as $syohinCode) {
        $files = $this->findImages($syohinCode);
        $fileList = array_merge($fileList, $files);
      }

      $fs = new Filesystem();
      $outputDir = sprintf('%s/images', $fileUtil->getDataDir());
      if (!$fs->exists($outputDir)) {
        $fs->mkdir($outputDir, 0755);
      }

      $this->archiveImageFiles($fileList, $outputDir);

    } catch (\Exception $e) {
      $logger->error('臨時 ppm 商品画像zip圧縮処理 エラー:' . $e->getMessage());
    }

    $logger->info('臨時 ppm 商品画像zip圧縮処理 完了');
    $output->writeln('done!');
  }

  private function findImages($syohinCode)
  {
    $imageDir = $this->getContainer()->getParameter('product_image_dir');
    $fs = new Filesystem();

    /** @var ProductImagesRepository $repo */
    $repo = $this->getDoctrine()->getRepository('MiscBundle:ProductImages');

    $imageFiles = [];

    $images = $repo->findProductImages($syohinCode);
    foreach($images as $image) {
      $dir = $image->getDirectory();
      $filename = $image->getFilename();
      $filePath = sprintf('%s/%s/%s', $imageDir, $dir, $filename);
      $localDir = sprintf('itemroot/%s', $dir);

      if ($fs->exists($filePath)) {
        $file = [
            'path' => $filePath
          , 'filename' => basename($filePath)
          , 'local_dir' => $localDir
        ];

        $imageFiles[] = $file;

      } else {
        $this->results['file_missing'][] = sprintf('%s : %s (local)', $image->getDaihyoSyohinCode(), $filePath);
      }
    }

    return $imageFiles;
  }


  private function archiveImageFiles($fileList, $distDir)
  {
    $logger = $this->getLogger();

    $zipFiles = [];
    $totalSize = 0;
    $num = 0;

    if ($fileList) {
      $dateTime = new \DateTime();
      $dateTime->modify('+5 minutes'); // 5分後に取り込まれる様に。
      $index = 1;
      $limitSize = 950000000; // 1GB 余裕を見て950MB
      $currentSize = 0;

      foreach($fileList as $file) {
        if (!isset($zip)) {
          $zip = new \ZipArchive();
          $fileName = sprintf('%s/img%s%02d.zip', $distDir, $dateTime->format('YmdHi'), $index++);
          if (! $zip->open($fileName, \ZipArchive::CREATE)) {
            throw new \RuntimeException('can not create image zip file. aborted. [' . $fileName . ']');
          }

          $zipFiles[] = $fileName;

          $logger->info('create zip file: ' . $fileName);
        }

        $filePath = $file['path'];
        $localName = $file['filename'];
        $localDir = $file['local_dir'];

        $zip->addFile($filePath, $localDir . '/' . $localName);
        $currentSize += filesize($filePath);
        $totalSize += filesize($filePath);
        $num++;

        $logger->info('add file: ' . $localDir . '/' . $localName . ' : zip total ' . $currentSize . ' bytes');

        // 閉じてオブジェクト削除（次のファイル作成の判定のため）
        if ($currentSize >= $limitSize) {
          $zip->close();
          $currentSize = 0;
          unset($zip);
        }
      }

      // 最後のファイルを閉じる
      if (isset($zip)) {
        $zip->close();
      }
    }

    return $zipFiles;
  }

