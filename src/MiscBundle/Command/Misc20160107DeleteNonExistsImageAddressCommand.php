  protected function execute(InputInterface $input, OutputInterface $output)
  {
    $this->input = $input;
    $this->output = $output;

    $this->logger = $this->getContainer()->get('misc.util.batch_logger');

    $container = $this->getContainer();
    $doctrine = $container->get('doctrine');

    $this->commonUtil = $container->get('misc.util.db_common');

    $fileUtil = $container->get('misc.util.file');
    $output->writeln($fileUtil->getRootDir());

    $db = $doctrine->getConnection('main');
    $list = $this->getList();
    foreach($list as $ele) {
      list($daihyoSyohinCode, $code) = explode("\t", $ele);
      $captionFieldFormat = preg_match('/^p/', $code) ? '商品画像%sCption' : '商品画像%sCaption';
      $updates = [
          sprintf($captionFieldFormat . ' = NULL', strtoupper($code))
        , sprintf('商品画像%sAdress = NULL', strtoupper($code))
        , sprintf('picname%s = NULL', strtoupper($code))
        , sprintf('picfolder%s = NULL', strtoupper($code))
      ];

      $setString = implode(", ", $updates);
      $sql = <<<EOD
        UPDATE tb_mainproducts
        SET
          {$setString}
        WHERE daihyo_syohin_code = :daihyoSyohinCode
EOD;

      $stmt = $db->prepare($sql);
      $stmt->bindValue(':daihyoSyohinCode', $daihyoSyohinCode, \PDO::PARAM_STR);
      $stmt->execute();
    }

    $output->writeln('done!');
  }

